package cf.e3ndr.UltimateLib.Wrappers.Util;

/**
 * The Interface Comparable.
 */
public interface IComparable {
	
	/**
	 * As string.
	 *
	 * @return the string
	 */
	public String asString();
	
	/**
	 * Compares two objects to test if they are equal
	 *
	 * @param o the object
	 * @return true, if equal
	 */
	public boolean compareEqual(Object o);
	
}
